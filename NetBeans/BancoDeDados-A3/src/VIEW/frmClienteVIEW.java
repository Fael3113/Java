    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome, cpf, email, pagamento, endereco;
        
        nome = txtNome.getText();
        cpf = txtCPF.getText();
        email = txtEmail.getText();
        pagamento = txtFormaPagamento.getText();
        endereco = txtEnderco.getText();
        
        ClienteDTO objClienteDTO = new ClienteDTO();
        objClienteDTO.setNome_cliente(nome);
        objClienteDTO.setCpf_cliente(cpf);
        objClienteDTO.setEmail_cliente(email);
        objClienteDTO.setEndereco_cliente(endereco);
        objClienteDTO.setPagamento_cliente(pagamento);
        
        ClienteDAO objClienteDAO = new ClienteDAO();
        try {
            objClienteDAO.cadastrarCliente(objClienteDTO);
        } catch (SQLException ex) {
            Logger.getLogger(frmClienteVIEW.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

